namespace Kojiro_ordering_management_system.用户端
{
    internal class Anchor
    {
        private int left;
        private int right;
        private int top;
        private int bottom;

        public Anchor(int left, int right, int top, int bottom)
        {
            this.left = left;
            this.right = right;
            this.top = top;
            this.bottom = bottom;
        }
    }
}